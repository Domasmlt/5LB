    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed

        JFileChooser langas = new JFileChooser();
        langas.setFileFilter(new FileNameExtensionFilter("txt failas", "txt"));
        int rez = langas.showOpenDialog(null);

        if (rez == JFileChooser.APPROVE_OPTION) {
            File failas = langas.getSelectedFile();

            try {
                String pirmaEilute = null;
                BufferedReader in = new BufferedReader(new FileReader(failas));
                String s;

                String temp = null;
                String kt = "Kategorijos:";
                String pj = "Pajamos:";
                String is = "Islaidos:";

                while ((s = in.readLine()) != null) {

                    if (s.trim().endsWith(":")) {
                        temp = s;
                    } else {
                        if (temp.contentEquals(kt)) {
                            String[] duom = s.split(",");
                            String pava = duom[0];
                            String aprasymas = duom[1];
                            abc.pridetiKategorija(pava, aprasymas);
                        } else if (temp.contentEquals(pj)) {
                            String[] duom = s.split(",");
                            Double sum = Double.parseDouble(duom[0]);
                            String pav = duom[1];
                            String is_kategorija = duom[2];
                            String komentaras = duom[3];
                            abc.pridetiPajamas(sum, new Date(), pav, is_kategorija, komentaras);
                        } else {
                            String[] duom = s.split(",");
                            Double sum = Double.parseDouble(duom[0]);
                            String pav = duom[1];
                            String is_kategorija = duom[2];
                            String komentaras = duom[3];
                            String num = duom[4];
                            abc.pridetiIslaida(sum, new Date(), pav, is_kategorija, komentaras, num);
                        }
                    }
                }
            } catch (Exception klaida) {
                System.out.println("Klaida");
                klaida.printStackTrace();
            } finally {
                System.out.println("Duomenys ikelti");
                JOptionPane.showMessageDialog(this, "Duomenys ikelti");
                atnaujintiIslaiduLentele();
                atnaujintiPajamuLentele();
                atnaujintiIslaiduBalansa();
                atnaujintiPajamuBalansa();
                atnaujintiBendraBalansa();
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

