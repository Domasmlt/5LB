    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed

        ArrayList<Kategorija> kat = abc.gautiKategorijuSarasa();
        ArrayList<Islaidos> isl = abc.gautiIslaiduSarasa();
        ArrayList<Pajamos> prp = abc.gautiPajamuSarasa();

        JFileChooser langas = new JFileChooser();
        langas.setFileFilter(new FileNameExtensionFilter("txt failas", "txt"));
        int rez = langas.showSaveDialog(this);
        if (rez == JFileChooser.APPROVE_OPTION) {
            try {
                FileWriter file = new FileWriter(new File(langas.getSelectedFile() + ""));
                for (Pajamos k : prp) {
                    file.write(k.toString());
                    file.write(String.format("%n"));
                }
                for (Islaidos k : isl) {
                    file.write(k.toString());
                    file.write(String.format("%n"));
                }
                for (Kategorija k : kat) {
                    file.write(k.toString());
                    file.write(String.format("%n"));
                }
                JOptionPane.showMessageDialog(this, "Duomenys issaugoti");
                file.close();
            } catch (Exception e) {
                System.out.println("Unable to perform operation");
            }
        }


    }//GEN-LAST:event_jMenuItem3ActionPerformed

